/*
 Estrutura de Repetição
1º FOR
A presentar na tela os numeros de 0 a 100.
 */

public class Exercicio01 {
    public static void main(String[] args) {
        System.out.println("Numeros de 0 a 100");
        for(int c=100;c>=0;c--)
        {
            System.out.println(c);
               
        }
    }
    
}
